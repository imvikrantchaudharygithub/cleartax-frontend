export function getIPOCategoryBySlug(slug: string): IPOCategory | undefined {
  return ipoCategories.find(category => category.slug === slug);
}

export function getIPOSubServiceBySlug(categorySlug: string, subServiceSlug: string): IPOSubService | undefined {
  const category = getIPOCategoryBySlug(categorySlug);
  return category?.subServices.find(service => service.slug === subServiceSlug);
}

